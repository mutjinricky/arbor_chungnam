const SCORE_DEFAULT = 50

/**
 * main_species 문자열에서 수종 취약성 점수를 산출한다.
 * 여러 수종이 +, 와(과), / 등으로 결합된 경우 가장 취약한 수종 기준 (보수적).
 */
export function vegetationScoreFromSpecies(mainSpecies) {
  if (!mainSpecies) return SCORE_DEFAULT
  const cleaned = String(mainSpecies)
    .replace(/\(.*?\)/g, '')
    .replace(/\d+\s*ÁÖ/gi, '')
    .replace(/\s+/g, '')
    .trim()
  if (!cleaned) return SCORE_DEFAULT

  // 분리: +, /, ,, 와, 과
  const parts = cleaned
    .split(/[+/,]|과|와/)
    .map((s) => s.trim())
    .filter(Boolean)

  if (parts.length === 0) return SCORE_DEFAULT

  const scores = parts.map((p) => lookupSpecies(p))
  // 가장 취약한 수종 기준 (방어적 점수)
  return Math.max(...scores)
}

function lookupSpecies(s) {
  if (SPECIES_VULNERABILITY[s] != null) return SPECIES_VULNERABILITY[s]

  // 부분 매칭: 키워드가 입력 또는 키에 포함되는 경우
  const keys = Object.keys(SPECIES_VULNERABILITY)

  // 1) 입력이 매트릭스 키를 포함 (예: "왕벚나무외1종" → "왕벚나무")
  for (const k of keys) {
    if (k.length >= 2 && s.includes(k)) return SPECIES_VULNERABILITY[k]
  }
  // 2) 매트릭스 키가 입력을 포함 (예: "벚" → "벚나무")
  for (const k of keys) {
    if (s.length >= 2 && k.includes(s)) return SPECIES_VULNERABILITY[k]
  }

  // 3) 휴리스틱: 끝 글자 기반 추론
  if (s.endsWith('소나무') || s.includes('소나무')) return 85
  if (s.endsWith('잣나무')) return 70
  if (s.endsWith('단풍')) return 40
  if (s.endsWith('나무')) return SCORE_DEFAULT

  return SCORE_DEFAULT
}
